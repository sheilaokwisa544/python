# Arithmetic operators
num1 = 56
num2 = 2
# addition
print(num1 + num2)
# sub
print(num1 - num2)
# multiplication
print(num1 * num2)
# division
print(num1 / num2)
# modulus returns the remainder after the division
print(num1 % num2)

# comparison operators
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2)
print(num1 != num2)

# Assignment operators
x = 14
print(x)
# addition assignment
x += 2
print(x)
x -= 2
print(x)
x *= 2
print(x)
x /= 2
print(x)
x %= 2
print(x)

# logic operators - and, or, not
number = 5
print(number < 10 and number > 20) # it returns a true if all conditions are true

print(number < 10 or number > 20) # it returns a true if any conditions are true
print((not number < 10 and number > 20)) # it returns a false

# operator precedence

result = (5+10*2/1-4)
print(result)

